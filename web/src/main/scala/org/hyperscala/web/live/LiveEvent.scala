package org.hyperscala.web.live

import org.hyperscala.javascript.JavaScriptString
import org.hyperscala.html.HTMLTag
import org.powerscala.event.Event

/**
 * @author Matt Hicks <mhicks@powerscala.org>
 */
class LiveEvent(tag: HTMLTag) extends Event

object LiveEvent extends JavaScriptString("liveEventHandler(event); return false;") {
  def apply(tag: HTMLTag, eventType: String) = eventType match {
    case "onafterprint" => new AfterPrintEvent(tag)
    case "onbeforeprint" => new BeforePrintEvent(tag)
    case "onbeforeonload" => new BeforeOnLoadEvent(tag)
    case "onhaschange" => new HasChangeEvent(tag)
    case "onload" => new LoadEvent(tag)
    case "onmessage" => new MessageEvent(tag)
    case "onoffline" => new OfflineEvent(tag)
    case "ononline" => new OnlineEvent(tag)
    case "onpagehide" => new PageHideEvent(tag)
    case "onpageshow" => new PageShowEvent(tag)
    case "onpopstate" => new PopStateEvent(tag)
    case "onredo" => new RedoEvent(tag)
    case "onresize" => new ResizeEvent(tag)
    case "onstorage" => new StorageEvent(tag)
    case "onundo" => new UndoEvent(tag)
    case "onunload" => new UnLoadEvent(tag)
    case "onblur" => new BlurEvent(tag)
    case "onchange" => new ChangeEvent(tag)
    case "oncontextmenu" => new ContextMenuEvent(tag)
    case "onfocus" => new FocusEvent(tag)
    case "onformchange" => new FormChangeEvent(tag)
    case "onforminput" => new FormInputEvent(tag)
    case "oninput" => new InputEvent(tag)
    case "oninvalid" => new InvalidEvent(tag)
    case "onreset" => new ResetEvent(tag)
    case "onselect" => new SelectEvent(tag)
    case "onsubmit" => new SubmitEvent(tag)
    case "onkeydown" => new KeyDownEvent(tag)
    case "onkeypress" => new KeyPressEvent(tag)
    case "onkeyup" => new KeyUpEvent(tag)
    case "onclick" => new ClickEvent(tag)
    case "ondblclick" => new DoubleClickEvent(tag)
    case "ondrag" => new DragEvent(tag)
    case "ondragend" => new DragEndEvent(tag)
    case "ondragenter" => new DragEnterEvent(tag)
    case "ondragleave" => new DragLeaveEvent(tag)
    case "ondragover" => new DragOverEvent(tag)
    case "ondragstart" => new DragStartEvent(tag)
    case "ondrop" => new DropEvent(tag)
    case "onmousedown" => new MouseDownEvent(tag)
    case "onmousemove" => new MouseMoveEvent(tag)
    case "onmouseout" => new MouseOutEvent(tag)
    case "onmouseover" => new MouseOverEvent(tag)
    case "onmouseup" => new MouseUpEvent(tag)
    case "onmousewheel" => new MouseWheelEvent(tag)
    case "onscroll" => new ScrollEvent(tag)
    case "onabort" => new AbortEvent(tag)
    case "oncanplay" => new CanPlayEvent(tag)
    case "oncanplaythrough" => new CanPlayThroughEvent(tag)
    case "ondurationchange" => new DurationChangeEvent(tag)
    case "onemptied" => new EmptiedEvent(tag)
    case "onended" => new EndedEvent(tag)
    case "onerror" => new ErrorEvent(tag)
    case "onloadeddata" => new LoadedDataEvent(tag)
    case "onloadedmetadata" => new LoadedMetaDataEvent(tag)
    case "onloadstart" => new LoadStartEvent(tag)
    case "onpause" => new PauseEvent(tag)
    case "onplay" => new PlayEvent(tag)
    case "onplaying" => new PlayingEvent(tag)
    case "onprogress" => new ProgressEvent(tag)
    case "onratechange" => new RateChangeEvent(tag)
    case "onreadystatechange" => new ReadyStateChangeEvent(tag)
    case "onseeked" => new SeekedEvent(tag)
    case "onseeking" => new SeekingEvent(tag)
    case "onstalled" => new StalledEvent(tag)
    case "onsuspend" => new SuspendEvent(tag)
    case "ontimeupdate" => new TimeUpdateEvent(tag)
    case "onvolumechange" => new VolumeChangeEvent(tag)
    case "onwaiting" => new WaitingEvent(tag)
  }
}

